    private void setMemoria(int cantidad) {
        main.memoria = main.generarProcesosAleatorios(cantidad);

        Iterator<DefaultProceso> i = main.memoria.iterator();
        StringBuilder builder = new StringBuilder();
        while (i.hasNext()) {
            
            DefaultProceso p = i.next();
            builder.append(p.toString()+"\n");
        }
        txtMemoria.setText(builder.toString());
    }

